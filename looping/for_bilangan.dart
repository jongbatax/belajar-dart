void main() {
  /* set loop dari 1 s/d 10
   * buat i tipe integer nilainya isi dengan 1
   * kemudian buat kondisi dimana i<=10,
   * jika kondisi benar maka jalankan blok dari loop mulai dari { s/d }
   * tambahkan i dengan 1 yaitu i++
   */
  for (int i = 1; i <= 10; i++) {
    print(i);
  }
}
